def metade(preço):
    res = preço / 2
    return res

def dobro(preço):
    res = preço * 2
    return res

def aumentar(preço, porcentagem):
    res =  preço + preço * (porcentagem / 100)
    return res
def diminuir(preço, porcentagem):
    res = preço - preço * (porcentagem / 100)  
    return res



